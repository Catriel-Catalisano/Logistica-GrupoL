    function validateHeaders(headers) {
        const requiredHeaders = ['recorrido', 'demandakg'];
        return requiredHeaders.every(header => headers.includes(header));
    }

    function processAssignments(dataRows, headers) {
        const assignments = [];

        dataRows.forEach(row => {
            const recorrido = row[headers.indexOf('recorrido')];
            const peso = parseFloat(row[headers.indexOf('demandakg')]);

            const unit = findBestFitUnit(peso);
            if (unit) {
                assignedChoferes.add(unit.chofer);
                assignments.push(createAssignment(recorrido, peso, unit));
            } else {
                assignments.push(createUnassigned(recorrido, peso, 'Capacidad insuficiente o chofer no disponible'));
            }
        });

        return assignments;
    }

